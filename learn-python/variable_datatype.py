# bool -> Boolean (True/False)
is_student = False
# list -> List (ordered collection) Ordered, changeable list
fruits = ["apple", "banana", "cherry"]
# dict -> Dictionary (key-value pairs)
person = {"name": "Alice", "age": 30}
# tuple -> Tuple (ordered, immutable collection) Ordered, unchangeable list
point = (10, 20, 400)
# set -> Set (unordered collection of unique items)
colors = {"red", "green"}


